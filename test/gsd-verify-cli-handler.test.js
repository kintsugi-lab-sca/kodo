describe('runGsdVerifyCli — Plane comment summary block (D-15)', () => {
  it('SUM1: summary block contiene primeras 3 líneas de comment_body (slice, NO re-render)', async () => {
    const { createFormatter } = await import('../src/cli/format.js');
    const stdout = makeStdoutStub();
    const fakeResult = {
      verdict: { action: 'pass', phase_id: '10', must_haves: 8 },
      plane: {
        commented: true,
        transitioned: true,
        comment_body: 'LINE1\nLINE2\nLINE3\nLINE4\nLINE5',
      },
      session: { session_id: 's1', task_ref: 'KL-42', phase_id: '10' },
    };
    await runGsdVerifyCli(
      { sessionId: 's1' },
      {
        runVerifyFn: async () => fakeResult,
        writeFn: stdout.write,
        errFn: () => {},
        formatterFn: () => createFormatter({ isTTY: false }, {}),
      },
    );
    const out = stdout.get();
    assert.match(out, /Plane comment \(summary\):\n  LINE1\n  LINE2\n  LINE3\n/);
    assert.doesNotMatch(out, /LINE4/, 'slice(0, 3) excluye líneas 4+');
    assert.doesNotMatch(out, /LINE5/);
  });

  it('SUM2: summary block aparece DESPUÉS de Verdict y ANTES de "Plane: commented="', async () => {
    const { createFormatter } = await import('../src/cli/format.js');
    const stdout = makeStdoutStub();
    await runGsdVerifyCli(
      { sessionId: 's1' },
      {
        runVerifyFn: async () => passResult(),
        writeFn: stdout.write,
        errFn: () => {},
        formatterFn: () => createFormatter({ isTTY: false }, {}),
      },
    );
    const out = stdout.get();
    const verdictIdx = out.indexOf('Verdict:');
    const summaryIdx = out.indexOf('Plane comment (summary):');
    const planeStatusIdx = out.indexOf('Plane: commented=');
    assert.ok(verdictIdx >= 0, 'sección Verdict presente');
    assert.ok(summaryIdx > verdictIdx, 'summary aparece después de Verdict');
    assert.ok(planeStatusIdx > summaryIdx, 'Plane: commented= aparece después del summary');
  });

  it('SUM3: summary block presente para los 4 verdicts', async () => {
    const { createFormatter } = await import('../src/cli/format.js');
    const cases = [
      { name: 'pass', result: passResult() },
      { name: 'fail', result: failResult() },
      { name: 'missing', result: missingResult() },
      { name: 'malformed', result: malformedResult() },
    ];
    for (const c of cases) {
      const stdout = makeStdoutStub();
      await runGsdVerifyCli(
        { sessionId: 's1' },
        {
          runVerifyFn: async () => c.result,
          writeFn: stdout.write,
          errFn: () => {},
          formatterFn: () => createFormatter({ isTTY: false }, {}),
        },
      );
      assert.match(stdout.get(), /Plane comment \(summary\):/, `summary block esperado para ${c.name}`);
      // Header determinista de verify.js debe aparecer en summary slice (slice de comment_body):
      assert.match(stdout.get(), /\[kodo:gsd\]/, `header determinista presente en summary (${c.name})`);
    }
  });
});

