/**
 * Captures writes to stdout/stderr for assertion.
 */
function makeStdoutStub() {
  let buf = '';
  return {
    write: (s) => {
      buf += s;
    },
    get: () => buf,
  };
}

// Plan 15-04 Task 1: result.plane.comment_body now exposes the markdown
// generated by renderComment. Builders include realistic comment_body strings
// so that Task 2 summary-slice tests have deterministic input.

/** Shorthand builder for a pass-verdict result from runGsdVerify. */
function passResult() {
  return {
    verdict: { action: 'pass', phase_id: '10', must_haves: 8 },
    plane: {
      commented: true,
      transitioned: true,
      comment_body: [
        '[kodo:gsd] ✅ Phase 10 verificada — Orchestrator gate',
        '',
        '- Must-haves: 8/8 verificados',
        '- Gaps: 0',
        '- Transicionada a Review',
      ].join('\n'),
    },
    session: { session_id: 's1', task_ref: 'KL-42', phase_id: '10' },
  };
}

function failResult() {
  return {
    verdict: {
      action: 'fail',
      phase_id: '10',
      reason: 'gaps-found',
      detail: 'gaps_count=2',
    },
    plane: {
      commented: true,
      transitioned: false,
      comment_body: [
        '[kodo:gsd] ❌ Phase 10 bloqueada — Orchestrator gate',
        '',
        'Motivo: gaps_count=2. Corrige VERIFICATION.md y re-dispara el flujo.',
      ].join('\n'),
    },
    session: { session_id: 's1', task_ref: 'KL-42', phase_id: '10' },
  };
}

function missingResult() {
  return {
    verdict: { action: 'missing', phase_id: '10' },
    plane: {
      commented: true,
      transitioned: false,
      comment_body: [
        '[kodo:gsd] ⚠️ VERIFICATION.md no encontrado para Phase 10',
        '',
        'Ejecuta `/gsd-verify-work` en la sesión y re-dispara el flujo.',
      ].join('\n'),
    },
    session: { session_id: 's1', task_ref: 'KL-42', phase_id: '10' },
  };
}

function malformedResult() {
  return {
    verdict: {
      action: 'malformed',
      phase_id: '10',
      detail: 'missing field must_haves_total',
    },
    plane: {
      commented: true,
      transitioned: false,
      comment_body: [
        '[kodo:gsd] ⚠️ VERIFICATION.md presente pero inválido (Phase 10)',
        '',
        'Detalle: missing field must_haves_total',
        'Corrige el frontmatter y re-dispara el flujo.',
      ].join('\n'),
    },
    session: { session_id: 's1', task_ref: 'KL-42', phase_id: '10' },
  };
}

